// 1. Sayı İşlemleri
// Bir dairenin yarıçapını kullanıcıdan alarak dairenin alanını ve çevresini
// hesaplayan bir program yazın. (Dairenin alanı: πr², çevresi: 2πr)

// Kullanıcıdan yarıçapı al
let radius = parseFloat(prompt("Dairenin yarıçapını girin:"));

// π sayısını tanımla
const pi = Math.PI;

// Dairenin alanını hesapla
let area = pi * Math.pow(radius, 2);

// Dairenin çevresini hesapla
let circumference = 2 * pi * radius;

// Sonuçları konsola yazdır
console.log(`Dairenin alanı: ${area}`);
console.log(`Dairenin çevresi: ${circumference}`);


